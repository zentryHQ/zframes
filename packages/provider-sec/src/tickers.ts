/** Zero-pad a CIK to the 10-digit form the submissions endpoint expects. */
export function padCik(cik: number | string): string {
  return String(cik).replace(/\D/g, "").padStart(10, "0");
}

/**
 * Resolve a user-supplied symbol to a 10-digit CIK string.
 * Accepts a bare ticker ("AAPL"), a HIP-3-prefixed symbol ("xyz:TSLA" → TSLA),
 * a raw CIK ("320193"), or a "CIK0000320193" form. Returns null if a ticker is
 * not in the bundled map and the input is not already a numeric CIK.
 */
export function resolveCik(tickerOrCik: string): string | null {
  const raw = tickerOrCik.trim();
  // "CIK0000320193" or a bare number → already a CIK.
  const cikMatch = /^cik[-\s]?(\d{1,10})$/i.exec(raw) ?? /^(\d{1,10})$/.exec(raw);
  if (cikMatch) return cikMatch[1].padStart(10, "0");
  // Strip an exchange/dex prefix like "xyz:TSLA" → "TSLA".
  const ticker = (raw.includes(":") ? raw.slice(raw.lastIndexOf(":") + 1) : raw)
    .toUpperCase()
    .replace(/[^A-Z0-9.-]/g, "");
  const cik = TICKER_TO_CIK[ticker];
  return cik === undefined ? null : padCik(cik);
}
